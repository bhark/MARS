/// Emit the chart's ClusterRole template (Helm directives inlined) on stdout.
/// Used by `mars-operator print-clusterrole` and by the chart drift check.
pub(crate) fn print_clusterrole() -> Result<()> {
    let mut out = String::new();
    out.push_str("{{- if .Values.rbac.create -}}\n");
    out.push_str("# Auto-generated by scripts/generate-clusterrole.sh. Do not edit by hand.\n");
    out.push_str("# Source of truth: bin/mars-operator/src/clusterrole.rs\n");
    out.push_str("apiVersion: rbac.authorization.k8s.io/v1\n");
    out.push_str("kind: ClusterRole\n");
    out.push_str("metadata:\n");
    out.push_str("  name: {{ include \"mars-operator.fullname\" . }}\n");
    out.push_str("  labels:\n");
    out.push_str("    {{- include \"mars-operator.labels\" . | nindent 4 }}\n");
    out.push_str("rules:\n");
    for (i, section) in RULES.iter().enumerate() {
        if i > 0 {
            out.push('\n');
        }
        for line in section.comment {
            writeln!(out, "  # {line}")?;
        }
        for rule in section.rules {
            writeln!(out, "  - apiGroups: {}", flow_array(rule.api_groups))?;
            writeln!(out, "    resources: {}", flow_array(rule.resources))?;
            writeln!(out, "    verbs: {}", flow_array(rule.verbs))?;
        }
    }
    out.push_str("{{- end -}}\n");
    print!("{out}");
    Ok(())
}

fn flow_array(items: &[&str]) -> String {
    let inner: Vec<String> = items.iter().map(|s| format!("{s:?}")).collect();
    format!("[{}]", inner.join(", "))
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn flow_array_quotes_and_separates() {
        assert_eq!(flow_array(&["get", "list"]), r#"["get", "list"]"#);
        assert_eq!(flow_array(&[""]), r#"[""]"#);
        assert_eq!(flow_array(&[]), "[]");
    }

    #[test]
    fn every_rule_has_verbs() {
        for section in RULES {
            for rule in section.rules {
                assert!(!rule.verbs.is_empty(), "rule {rule:?} has no verbs");
                assert!(!rule.resources.is_empty(), "rule {rule:?} has no resources");
            }
        }
    }
}
