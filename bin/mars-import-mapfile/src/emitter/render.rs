/// Logical source ids the emitter binds layers to. These must match the ids
/// the operator's `MarsServiceCluster.sourcesCatalog[].id` exposes; the CLI
/// surfaces them as `--postgis-source-id` / `--vectorfile-source-id`.
#[derive(Debug, Clone)]
pub(crate) struct SourceIds {
    pub(crate) postgis: SourceId,
    pub(crate) vectorfile: SourceId,
}

impl Default for SourceIds {
    fn default() -> Self {
        Self {
            postgis: SourceId::new("pg"),
            vectorfile: SourceId::new("ogr"),
        }
    }
}

pub(crate) fn render(skel: &Skeleton, bands: &[(String, u64)], source_ids: &SourceIds) -> String {
    let def = build_definition(skel, bands, source_ids);
    let mut yaml = String::from("# Generated by mars-import-mapfile\n");
    match serde_yaml_ng::to_string(&def) {
        Ok(body) => yaml.push_str(&body),
        Err(e) => {
            warn!(error = %e, "serde_yaml_ng failed to serialize RenderDefinition");
        }
    }
    yaml
}

fn build_definition(skel: &Skeleton, bands: &[(String, u64)], source_ids: &SourceIds) -> RenderDefinition {
    let has_vectorfile = skel
        .layers
        .iter()
        .flat_map(|l| &l.sources)
        .any(|s| matches!(s.source, BindingSource::VectorFile(_)));

    RenderDefinition {
        service: build_service_meta(skel),
        scales: Scales {
            bands: bands
                .iter()
                .map(|(name, cap)| Band {
                    name: name.clone(),
                    max_denom: *cap,
                })
                .collect(),
        },
        interfaces: build_interfaces(skel),
        tile_matrix_sets: BTreeMap::new(),
        reprojection: Reprojection::default(),
        styles: build_styles(skel),
        layers: build_layers(skel, bands, has_vectorfile, source_ids),
    }
}

fn build_service_meta(skel: &Skeleton) -> ServiceMeta {
    let name = skel.service_name.clone().unwrap_or_else(|| "unnamed".to_string());
    let svc = &skel.service_meta;
    let title = svc
        .title_override
        .clone()
        .or_else(|| skel.service_title.clone())
        .unwrap_or_else(|| name.clone());
    let abstract_ = svc
        .abstract_
        .clone()
        .unwrap_or_else(|| "Imported from mapfile".to_string());

    let contact_email_top = if svc.has_structured_contact() {
        String::new()
    } else {
        svc.contact_email
            .clone()
            .unwrap_or_else(|| "ops@example.org".to_string())
    };

    ServiceMeta {
        name,
        title,
        abstract_,
        contact_email: contact_email_top,
        fonts: Fonts::default(),
        scale_dpi: skel.scale_dpi.unwrap_or(96.0),
        contact: build_contact(svc),
        ows: build_service_ows(svc),
        wms: build_service_wms(svc),
    }
}

fn build_contact(svc: &ServiceMetaSkeleton) -> ContactInfo {
    let any_addr = svc.address_type.is_some()
        || svc.address_street.is_some()
        || svc.address_city.is_some()
        || svc.address_state.is_some()
        || svc.address_postcode.is_some()
        || svc.address_country.is_some();
    let has_inline_email = svc.has_structured_contact() && svc.contact_email.is_some();
    if !svc.has_structured_contact() && !has_inline_email {
        return ContactInfo::default();
    }
    ContactInfo {
        person: svc.contact_person.clone().unwrap_or_default(),
        position: svc.contact_position.clone().unwrap_or_default(),
        organization: svc.contact_organization.clone().unwrap_or_default(),
        phone: svc.contact_phone.clone().unwrap_or_default(),
        fax: svc.contact_fax.clone().unwrap_or_default(),
        email: if has_inline_email {
            svc.contact_email.clone().unwrap_or_default()
        } else {
            String::new()
        },
        address: if any_addr {
            Address {
                type_: svc.address_type.clone().unwrap_or_default(),
                street: svc.address_street.clone().unwrap_or_default(),
                city: svc.address_city.clone().unwrap_or_default(),
                state_or_province: svc.address_state.clone().unwrap_or_default(),
                postcode: svc.address_postcode.clone().unwrap_or_default(),
                country: svc.address_country.clone().unwrap_or_default(),
            }
        } else {
            Address::default()
        },
    }
}

fn build_service_ows(svc: &ServiceMetaSkeleton) -> ServiceOws {
    ServiceOws {
        keywords: svc.keywords.clone(),
        online_resource: svc.online_resource.clone(),
        fees: svc.fees.clone(),
        access_constraints: svc.access_constraints.clone(),
        encoding: svc.encoding.clone(),
    }
}

fn build_service_wms(svc: &ServiceMetaSkeleton) -> ServiceWms {
    ServiceWms {
        bbox_extended: svc.bbox_extended.unwrap_or(false),
        advertised_crs: svc.advertised_crs.clone(),
        sld_enabled: svc.sld_enabled.unwrap_or(false),
        authorities: svc
            .authorities
            .iter()
            .map(|(n, h)| ConfigAuthorityRef {
                name: n.clone(),
                href: h.clone(),
            })
            .collect(),
        identifiers: svc
            .identifiers
            .iter()
            .map(|(a, v)| IdentifierRef {
                authority: a.clone(),
                value: v.clone(),
            })
            .collect(),
        formats: mars_config::ServiceFormats {
            get_map: svc.getmap_formats.clone(),
            get_feature_info: svc.getfeatureinfo_formats.clone(),
            get_legend_graphic: svc.getlegend_formats.clone(),
        },
    }
}

fn build_interfaces(skel: &Skeleton) -> Interfaces {
    Interfaces {
        wms: Some(WmsConfig {
            enabled: true,
            versions: vec!["1.3.0".to_string()],
            formats: vec!["image/png".to_string(), "image/jpeg".to_string()],
            listen: None,
            max_image_dimension: skel.wms_max_image_dimension,
            max_pixels: None,
        }),
        wmts: None,
        tile_cache: None,
        cors: None,
    }
}

fn build_styles(skel: &Skeleton) -> BTreeMap<String, StyleEntry> {
    skel.styles
        .iter()
        .map(|s| (s.name.clone(), styledef_to_entry(s)))
        .collect()
}

fn styledef_to_entry(st: &StyleDef) -> StyleEntry {
    match st.style_type.as_str() {
        "label" => StyleEntry::Label(styledef_to_label(st)),
        "line" => StyleEntry::Line(styledef_to_style(st)),
        "polygon" => StyleEntry::Polygon(styledef_to_style(st)),
        "point" => StyleEntry::Point(styledef_to_style(st)),
        other => {
            warn!(style_type = %other, "unknown style_type from importer; defaulting to polygon");
            StyleEntry::Polygon(styledef_to_style(st))
        }
    }
}

fn styledef_to_label(st: &StyleDef) -> LabelStyle {
    let fill = match &st.fill {
        Some(EmitFill::Hex(c)) => *c,
        _ => Colour::rgb(0, 0, 0),
    };
    let halo = match (st.halo_color, st.halo_width) {
        (Some(c), w) => Some(mars_style::Halo {
            colour: c,
            width: w.unwrap_or(1.0),
        }),
        _ => None,
    };
    let angle = if let Some(col) = &st.angle_attribute {
        Some(NumericField::Attribute(col.clone()))
    } else {
        st.angle_deg.map(NumericField::Static)
    };
    LabelStyle {
        font_family: st.font_family.clone().unwrap_or_default(),
        font_size: ScaledSize::from_px(st.font_size.unwrap_or(10.0)),
        fill,
        halo,
        priority: st.priority.unwrap_or(0),
        min_distance: st.min_distance.unwrap_or(0.0),
        position: st.position.unwrap_or(AnchorPosition::Auto),
        offset_px: st.offset_px.unwrap_or((0.0, 0.0)),
        angle,
        partials: st.partials.unwrap_or(false),
        force: st.force.unwrap_or(false),
    }
}

fn styledef_to_style(st: &StyleDef) -> Style {
    Style {
        fill: st.fill.as_ref().map(emit_fill_to_paint),
        stroke: st.stroke,
        stroke_width: st.stroke_width.map(ScaledSize::from_px),
        stroke_dasharray: st.stroke_dasharray.clone(),
        stroke_linecap: st.stroke_linecap.and_then(parse_line_cap),
        stroke_linejoin: st.stroke_linejoin.and_then(parse_line_join),
        marker: st.marker.as_ref().map(emit_marker_to_symbol),
        opacity: st.opacity,
        stroke_offset_px: st.stroke_offset_px,
        stroke_gap: st.stroke_gap.map(|g| StrokeGap {
            interval_px: g.interval_px,
            initial_px: g.initial_px,
        }),
        geom_transform: st.geom_transform.and_then(parse_geom_transform),
        min_feature_size_px: st.min_feature_size_px,
        blend_mode: st.blend_mode,
    }
}

fn emit_fill_to_paint(f: &EmitFill) -> FillPaint {
    match f {
        EmitFill::Hex(c) => FillPaint::Solid(*c),
        EmitFill::Hatch {
            spacing,
            angle_deg,
            line_width,
            colour,
        } => FillPaint::Hatch {
            spacing: *spacing,
            angle_deg: *angle_deg,
            line_width: *line_width,
            colour: *colour,
        },
        EmitFill::Image { name } => FillPaint::Image { name: name.clone() },
    }
}

fn emit_marker_to_symbol(m: &EmitMarker) -> MarkerSymbol {
    match m {
        EmitMarker::Builtin {
            kind,
            size,
            size_attribute,
            angle,
        } => MarkerSymbol {
            shape: marker_kind_to_shape(*kind),
            size: scaled_size_with_attr(*size, size_attribute.as_deref()),
            angle: angle.as_ref().map(numeric_to_field),
        },
        EmitMarker::Vector {
            points,
            anchor,
            filled,
            size,
            size_attribute,
            angle,
        } => MarkerSymbol {
            shape: MarkerShape::VectorShape {
                points: points.clone(),
                anchor: anchor.unwrap_or((0.5, 0.5)),
                filled: *filled,
            },
            size: scaled_size_with_attr(*size, size_attribute.as_deref()),
            angle: angle.as_ref().map(numeric_to_field),
        },
        EmitMarker::Glyph {
            font_family,
            character,
            size,
            size_attribute,
            angle,
        } => MarkerSymbol {
            shape: MarkerShape::Glyph {
                font_family: font_family.clone(),
                ch: character.clone(),
            },
            size: scaled_size_with_attr(*size, size_attribute.as_deref()),
            angle: angle.as_ref().map(numeric_to_field),
        },
    }
}

fn marker_kind_to_shape(k: MarkerKind) -> MarkerShape {
    match k {
        MarkerKind::Circle => MarkerShape::Circle,
        MarkerKind::Square => MarkerShape::Square,
        MarkerKind::Triangle => MarkerShape::Triangle,
        MarkerKind::Cross => MarkerShape::Cross,
        MarkerKind::X => MarkerShape::X,
        MarkerKind::Pin => MarkerShape::Pin,
    }
}

fn scaled_size_with_attr(size_px: f32, attr: Option<&str>) -> ScaledSize {
    ScaledSize {
        base_px: size_px,
        min_px: None,
        max_px: None,
        ref_denom: None,
        attribute: attr.map(str::to_owned),
    }
}

fn numeric_to_field(n: &EmitNumeric) -> NumericField {
    match n {
        EmitNumeric::Static(v) => NumericField::Static(*v),
        EmitNumeric::Attribute(c) => NumericField::Attribute(c.clone()),
    }
}

fn parse_line_cap(s: &'static str) -> Option<mars_style::LineCap> {
    use mars_style::LineCap;
    match s {
        "butt" => Some(LineCap::Butt),
        "round" => Some(LineCap::Round),
        "square" => Some(LineCap::Square),
        _ => None,
    }
}

fn parse_line_join(s: &'static str) -> Option<mars_style::LineJoin> {
    use mars_style::LineJoin;
    match s {
        "miter" => Some(LineJoin::Miter),
        "round" => Some(LineJoin::Round),
        "bevel" => Some(LineJoin::Bevel),
        _ => None,
    }
}

fn parse_geom_transform(s: &'static str) -> Option<mars_style::GeomTransform> {
    use mars_style::GeomTransform;
    match s {
        "start" => Some(GeomTransform::Start),
        "end" => Some(GeomTransform::End),
        "vertices" => Some(GeomTransform::Vertices),
        _ => None,
    }
}

fn build_layers(skel: &Skeleton, bands: &[(String, u64)], has_vectorfile: bool, source_ids: &SourceIds) -> Vec<Layer> {
    let windows = band_windows(bands);
    skel.layers
        .iter()
        .map(|layer| build_layer(layer, &windows, has_vectorfile, source_ids))
        .collect()
}

fn build_layer(
    layer: &LayerSkeleton,
    windows: &[super::bands::BandWindow<'_>],
    has_vectorfile: bool,
    source_ids: &SourceIds,
) -> Layer {
    let kind = layer.geom_kind.clone().unwrap_or_else(|| "polygon".to_string());
    let band_tiers = split_layer_into_bands(layer, windows);
    let mut sources: Vec<SourceBinding> = Vec::new();
    for (band_name, tiers) in &band_tiers {
        for tier in tiers {
            sources.push(source_skeleton_to_binding(
                tier.src,
                band_name,
                tier.max_denom,
                has_vectorfile,
                source_ids,
            ));
        }
    }

    Layer {
        name: LayerId::new(layer.name.as_str()),
        title: layer.title.clone().unwrap_or_default(),
        abstract_: layer.abstract_.clone().unwrap_or_default(),
        kind,
        scale: None,
        group: layer.group.clone(),
        bbox: None,
        sources,
        classes: layer.classes.iter().map(class_skel_to_class).collect(),
        label: layer.label.as_ref().map(label_skel_to_layer_label),
        label_survival: mars_style::LabelSurvival::default(),
        raster: None,
        wms: build_layer_wms(&layer.wms),
        ows: build_layer_ows(&layer.ows),
        template: layer.template.clone(),
    }
}

fn source_skeleton_to_binding(
    src: &SourceSkeleton,
    band_name: &str,
    max_denom: Option<u64>,
    has_vectorfile: bool,
    source_ids: &SourceIds,
) -> SourceBinding {
    let source_id = if has_vectorfile {
        match &src.source {
            BindingSource::VectorFile(_) => source_ids.vectorfile.clone(),
            _ => source_ids.postgis.clone(),
        }
    } else {
        // single-source path: stick to the catalog default id so bindings
        // resolve without an explicit `source:` ref.
        SourceId::new(mars_config::DEFAULT_SOURCE_ID)
    };

    let kind = match &src.source {
        BindingSource::Table(from) => BindingKind::PostgisTable {
            from: from.clone(),
            geometry_column: src.geometry_column.clone(),
            dsn: None,
        },
        BindingSource::Sql(sql) => BindingKind::PostgisSql {
            sql: sql.clone(),
            geometry_column: src.geometry_column.clone(),
            dsn: None,
        },
        BindingSource::VectorFile(vf) => BindingKind::Vectorfile {
            uri: vf.uri.clone(),
            format: parse_vectorfile_format(&vf.format),
            source_crs: CrsCode::new(vf.source_crs.as_str()),
        },
    };

    SourceBinding {
        source: source_id,
        kind,
        scale: None,
        band: Some(band_name.to_string()),
        max_denom,
        filter: src.filter.clone(),
        id_column: src.id_column.clone(),
        attributes: src.attributes.clone(),
        levels: None,
        page_size_target_bytes: None,
        reconcile_every_cycles: None,
        sidecar_size_warn_bytes: None,
        simplifier: None,
        on_missing_page: None,
    }
}

fn parse_vectorfile_format(s: &str) -> VectorFileFormat {
    match s {
        "flat_geobuf" => VectorFileFormat::FlatGeobuf,
        "geo_json" => VectorFileFormat::GeoJson,
        "shapefile" => VectorFileFormat::Shapefile,
        "geo_package" => VectorFileFormat::GeoPackage,
        other => {
            warn!(format = %other, "unknown vectorfile format from importer; defaulting to geo_json");
            VectorFileFormat::GeoJson
        }
    }
}

fn class_skel_to_class(cls: &ClassSkeleton) -> Class {
    let style = match &cls.style {
        ClassStyleAttach::Ref(name) => ClassStyle::Ref { name: name.clone() },
        ClassStyleAttach::Passes(passes) => ClassStyle::Passes {
            passes: passes.iter().map(styledef_to_style).collect(),
        },
    };
    Class {
        name: cls.name.clone(),
        title: cls.title.clone().unwrap_or_default(),
        when: cls.when.clone(),
        scale: if cls.min_scale_denom.is_some() || cls.max_scale_denom.is_some() {
            Some(ScaleWindow {
                min: cls.min_scale_denom,
                max: cls.max_scale_denom,
            })
        } else {
            None
        },
        style,
        label: cls.label.as_ref().map(label_skel_to_layer_label),
    }
}

fn label_skel_to_layer_label(lbl: &LabelSkeleton) -> LayerLabel {
    LayerLabel {
        style: mars_config::LabelStyleAttach::Ref {
            name: lbl.style_ref.clone(),
        },
        text: lbl.text.clone(),
        placement: lbl.placement_line.map(line_placement_to_placement),
    }
}

fn line_placement_to_placement(p: EmitLinePlacement) -> Placement {
    // mirror mars_style::Placement::Line defaults verbatim so a missing
    // mapfile knob round-trips through the same value the wire-form default
    // would supply.
    Placement::Line {
        repeat_m: p.repeat_m.unwrap_or(250.0),
        max_angle_delta_deg: p.max_angle_delta_deg.unwrap_or(25.0),
        angle_mode: p.angle_mode.unwrap_or_default(),
    }
}

fn build_layer_wms(wms: &LayerWmsSkeleton) -> LayerWms {
    LayerWms {
        opaque: wms.opaque.unwrap_or(false),
        advertised_crs: if wms.advertised_crs.is_empty() {
            None
        } else {
            Some(wms.advertised_crs.clone())
        },
        enable_get_feature_info: false,
    }
}

fn build_layer_ows(ows: &LayerOwsSkeleton) -> LayerOws {
    LayerOws {
        keywords: ows.keywords.clone(),
        metadata_urls: ows
            .metadata_urls
            .iter()
            .map(|(t, f, h)| MetadataUrl {
                type_: t.clone(),
                format: f.clone(),
                href: h.clone(),
            })
            .collect(),
        authorities: ows
            .authorities
            .iter()
            .map(|(n, h)| ConfigAuthorityRef {
                name: n.clone(),
                href: h.clone(),
            })
            .collect(),
        identifiers: ows
            .identifiers
            .iter()
            .map(|(a, v)| IdentifierRef {
                authority: a.clone(),
                value: v.clone(),
            })
            .collect(),
        attribution: ows.attribution.as_ref().map(|a| ConfigAttribution {
            title: a.title.clone().unwrap_or_default(),
            online_resource: a.online_resource.clone(),
            logo: build_logo(a),
        }),
        include_items: ows
            .include_items
            .as_ref()
            .map(include_items_to_config)
            .unwrap_or_default(),
        request_gating: build_request_gating(ows),
    }
}

fn build_logo(a: &super::skeleton::LayerAttributionSkeleton) -> Option<LogoUrl> {
    if a.logo_format.is_none() && a.logo_href.is_none() && a.logo_width.is_none() && a.logo_height.is_none() {
        return None;
    }
    Some(LogoUrl {
        format: a.logo_format.clone().unwrap_or_default(),
        href: a.logo_href.clone().unwrap_or_default(),
        width: a.logo_width,
        height: a.logo_height,
    })
}

fn include_items_to_config(items: &IncludeItemsSkeleton) -> IncludeItems {
    match items {
        IncludeItemsSkeleton::All => IncludeItems {
            mode: IncludeMode::All,
            names: Vec::new(),
        },
        IncludeItemsSkeleton::None => IncludeItems {
            mode: IncludeMode::None,
            names: Vec::new(),
        },
        IncludeItemsSkeleton::Explicit(names) => IncludeItems {
            mode: IncludeMode::Explicit,
            names: names.clone(),
        },
    }
}

fn build_request_gating(ows: &LayerOwsSkeleton) -> BTreeMap<ServiceOp, bool> {
    let mut map = BTreeMap::new();
    let rg = &ows.request_gating;
    if let Some(b) = rg.get_capabilities {
        map.insert(ServiceOp::WmsGetCapabilities, b);
    }
    if let Some(b) = rg.get_map {
        map.insert(ServiceOp::WmsGetMap, b);
    }
    if let Some(b) = rg.get_feature_info {
        map.insert(ServiceOp::WmsGetFeatureInfo, b);
    }
    if let Some(b) = rg.get_legend_graphic {
        map.insert(ServiceOp::WmsGetLegendGraphic, b);
    }
    if let Some(b) = rg.get_styles {
        map.insert(ServiceOp::WmsGetStyles, b);
    }
    if let Some(b) = rg.describe_layer {
        map.insert(ServiceOp::WmsDescribeLayer, b);
    }
    if let Some(b) = rg.wmts_get_capabilities {
        map.insert(ServiceOp::WmtsGetCapabilities, b);
    }
    if let Some(b) = rg.wmts_get_tile {
        map.insert(ServiceOp::WmtsGetTile, b);
    }
    if let Some(b) = rg.wmts_get_feature_info {
        map.insert(ServiceOp::WmtsGetFeatureInfo, b);
    }
    map
}
