//! one-pass YAML emitter for the mapfile translator skeleton.
//!
//! intentionally string-based: this is scaffolding output, not round-trippable
//! YAML, and the result is meant to be hand-edited.

use std::fmt::Write as _;

#[derive(Debug, Default)]
pub(crate) struct Skeleton {
    pub(crate) service_name: Option<String>,
    pub(crate) service_title: Option<String>,
    pub(crate) layers: Vec<String>,
}

/// quote a YAML string using simple double-quoting; escapes `"` and `\`.
fn yaml_quote(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for ch in s.chars() {
        match ch {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            _ => out.push(ch),
        }
    }
    out.push('"');
    out
}

pub(crate) fn render(skel: &Skeleton) -> String {
    let mut out = String::new();
    out.push_str("# Generated by mars-import-mapfile (Phase 0 scaffolding).\n");
    out.push_str("# Hand-tune before use; styles, classes and expressions are NOT translated.\n\n");

    out.push_str("service:\n");
    let name = skel.service_name.as_deref().unwrap_or("unnamed");
    let _ = writeln!(out, "  name: {}", yaml_quote(name));
    if let Some(title) = &skel.service_title {
        let _ = writeln!(out, "  title: {}", yaml_quote(title));
    }
    out.push('\n');

    out.push_str("layers:\n");
    if skel.layers.is_empty() {
        out.push_str("  []\n");
    } else {
        for name in &skel.layers {
            let _ = writeln!(out, "  - name: {}", yaml_quote(name));
            let _ = writeln!(
                out,
                "    # TODO: layer {} — translate type, sources, classes from mapfile",
                name
            );
        }
    }
    out
}
