//! one-pass YAML emitter for the mapfile translator skeleton.
//!
//! intentionally string-based: this is scaffolding output, not round-trippable
//! YAML, and the result is meant to be hand-edited.

use std::fmt::Write as _;

#[derive(Debug, Default)]
pub(crate) struct Skeleton {
    pub(crate) service_name: Option<String>,
    pub(crate) service_title: Option<String>,
    pub(crate) layers: Vec<LayerSkeleton>,
}

#[derive(Debug, Default)]
pub(crate) struct LayerSkeleton {
    pub(crate) name: String,
    /// inclusive lower bound on scale denom (mapfile MINSCALEDENOM).
    pub(crate) min_scale_denom: Option<u64>,
    /// exclusive upper bound on scale denom (mapfile MAXSCALEDENOM).
    pub(crate) max_scale_denom: Option<u64>,
}

/// quote a YAML string using simple double-quoting; escapes `"` and `\`.
fn yaml_quote(s: &str) -> String {
    let mut out = String::with_capacity(s.len() + 2);
    out.push('"');
    for ch in s.chars() {
        match ch {
            '"' => out.push_str("\\\""),
            '\\' => out.push_str("\\\\"),
            _ => out.push(ch),
        }
    }
    out.push('"');
    out
}

pub(crate) fn render(skel: &Skeleton) -> String {
    let mut out = String::new();
    out.push_str("# Generated by mars-import-mapfile (Phase 0 scaffolding).\n");
    out.push_str("# INCOMPLETE: layer translation is a TODO; styles, classes,\n");
    out.push_str("# expressions, projections and metadata are NOT translated.\n");
    out.push_str("# Hand-tune every layer before use.\n\n");

    out.push_str("service:\n");
    let name = skel.service_name.as_deref().unwrap_or("unnamed");
    let _ = writeln!(out, "  name: {}", yaml_quote(name));
    if let Some(title) = &skel.service_title {
        let _ = writeln!(out, "  title: {}", yaml_quote(title));
    }
    out.push('\n');

    out.push_str("layers:\n");
    if skel.layers.is_empty() {
        out.push_str("  []\n");
    } else {
        for layer in &skel.layers {
            let _ = writeln!(out, "  - name: {}", yaml_quote(&layer.name));
            if layer.min_scale_denom.is_some() || layer.max_scale_denom.is_some() {
                out.push_str("    scale:\n");
                if let Some(v) = layer.min_scale_denom {
                    let _ = writeln!(out, "      min: {v}");
                }
                if let Some(v) = layer.max_scale_denom {
                    let _ = writeln!(out, "      max: {v}");
                }
            }
            let _ = writeln!(
                out,
                "    # TODO: layer {} — translate type, sources, classes from mapfile",
                layer.name
            );
        }
    }
    out
}
